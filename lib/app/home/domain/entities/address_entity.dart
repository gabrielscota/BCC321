class AddressEntity {
  final String id;
  final String street;
  final String number;
  final String city;
  final String state;
  final String country;
  final String zipCode;

  AddressEntity({
    required this.id,
    required this.street,
    required this.number,
    required this.city,
    required this.state,
    required this.country,
    required this.zipCode,
  });
}
